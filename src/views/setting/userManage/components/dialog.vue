<template>
  <!-- 用户导入对话框 -->
  <new-dialog
     width="460px"
    :show="upload.open"
    :title="upload.title"
    :on-close="closeDialog"
    :on-submit="submitFileForm"
  >
    <el-upload
        name="file"
        ref="upload"
        :limit="1"
        accept=".xlsx, .xls"
        :headers="upload.headers"
        :action="upload.url + '?updateSupport=' + upload.updateSupport"
        :disabled="upload.isUploading"
        :on-progress="handleFileUploadProgress"
        :on-success="handleFileSuccess"
        :auto-upload="false"
        drag
      >
    
      <i class="el-icon-upload"></i>
      <div class="el-upload__text">将文件拖到此处，或<em>点击上传</em></div>
      <div class="el-upload__tip text-center" slot="tip">
        <div class="el-upload__tip" slot="tip">
          <el-checkbox v-model="upload.updateSupport" />
          是否更新已经存在的用户数据
        </div>
        <span>仅允许导入xls、xlsx格式文件。</span>
        <el-link
          type="primary"
          :underline="false"
          style="font-size: 12px; vertical-align: baseline"
          @click="importTemplate"
          >下载模板</el-link
        >
      </div>
    </el-upload>
    <!-- <div slot="footer" class="dialog-footer">
        <el-button type="primary" @click="submitFileForm">确 定</el-button>
        <el-button @click="upload.open = false">取 消</el-button>
      </div> -->
  </new-dialog>
</template>
<script lang="ts">
import { Component, Prop, Watch, Vue } from "vue-property-decorator";
import NewDialog from "@/components/newDialog/index.vue";
import { getToken } from "@/utils/auth";
import { download } from "@/api/user/user";
import service from "@/utils/request";
@Component({
  components: {
    NewDialog,
  },
})
export default class UserDialog extends Vue {
  // 用户导入参数
  upload = {
    // 是否显示弹出层（用户导入）
    open: false,
    // 弹出层标题（用户导入）
    title: "用户导入",
    // 是否禁用上传
    isUploading: false,
    // 是否更新已经存在的用户数据
    updateSupport: 0,
    // 设置上传的请求头部
    headers: { Authorization: "Bearer " + getToken() },
    // 上传的地址 
    url: "/api/system/user/importData",
  };
  /** 导入按钮操作 */
  handleImport() {
    this.upload.title = "用户导入";
    this.upload.open = true;
  }
  /** 下载模板操作 */
  importTemplate() {
    download("/system/user/importTemplate");
  }
  // 文件上传中处理
  handleFileUploadProgress(event: any, file: any, fileList: any) {
    this.upload.isUploading = true;
  }
  // 文件上传成功处理
  handleFileSuccess(response: any, file: any, fileList: any) {
    this.upload.open = false;
    this.upload.isUploading = false;
    (this.$refs.upload as any).clearFiles();
    this.$alert(response.msg, "导入结果", { dangerouslyUseHTMLString: true });
    this.$emit("fetchData");
  }
  // 提交上传文件
  submitFileForm() {
    (this.$refs.upload as any).submit();
  }
  showDialog() {
    this.upload.open = true;
  }
  closeDialog() {
    this.upload.open = false;
  }
 
}
</script>
